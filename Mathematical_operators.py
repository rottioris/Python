z = 5
x = 2

print(f"{z} + {x} = {z+x}")
print(f"{z} - {x} = {z-x}")
print(f"{z} x {x} = {z*x}")
print(f"{z} / {x} = {z/x}")

y = 7
# Dividir al piso
print(f"{z} // {y} = {z//y}")

# Modulo 7/2 = 1 (ESTE ES EL MODULO)
print(f"{z} modulo de {y} = {z%y}") # Sirve para busar numero impares

print(f"{z} elevado a la {x} = {z**x}")
print(f"{z} elevado a la {3} = {z**3}")

# Raiz Cuadrada

print(f"La raiz cuadrada de {z} = {z**0.5}")